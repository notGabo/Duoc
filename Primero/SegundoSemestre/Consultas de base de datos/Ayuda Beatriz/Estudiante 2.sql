SELECT 
NUMPROPIEDAD AS "NRO PROPIEDAD",
RUTPRO AS "RUT PROPIEDAD",
DIRECCION AS "DIRECCIÓN",
RTRIM(TO_CHAR(RENTA,'$9G999G999')) AS "VALOR DEL ARRIENDO",
CASE 
    WHEN RENTA >=1000000 THEN TO_CHAR(ROUND((TO_NUMBER(REGEXP_SUBSTR(RENTA,'(\d)(\d)'))/100)*RENTA),'$9G999G999')
    WHEN RENTA <1000000 THEN TO_CHAR(ROUND((TO_NUMBER(REGEXP_SUBSTR(RENTA,'(\d)'))/100)*RENTA),'$9G999G999')
END AS COMPENSACION
FROM PROPIEDAD
ORDER BY 1 ASC;
