'El empleado '||upper(NOMBRE)||' '||upper(PATERNO)||' '||
upper(MATERNO)||' está de cumpleaños el '||TO_CHAR(fecnac,'DD')||' de '||TRIM(INITCAP(TO_CHAR(fecnac,'MONTH')))||'. Cumple '||
trunc(MONTHS_BETWEEN(ADD_MONTHS(SYSDATE,-1),FECNAC)/12)||' años.' as "LISTADO DE CUMPLEAÑOS"
