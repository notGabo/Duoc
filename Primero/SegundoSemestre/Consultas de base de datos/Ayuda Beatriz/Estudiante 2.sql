SELECT 
NOMBRE||' '||PATERNO||' '||MATERNO AS "NOMBRE COMPLETO",
TO_CHAR(FECING,'YYYY')||SUBSTR(LOWER(NOMBRE),1,1)||SUBSTR(LOWER(MATERNO),-2)||'@corredorah.cl' AS "CORREO EMPLEADO",
CASE 
    WHEN SUBSTR(PATERNO,1,1) BETWEEN 'A' AND 'E' THEN 'Lunes 24 de Septiembre'
    WHEN SUBSTR(PATERNO,1,1) BETWEEN 'F' AND 'J' THEN 'Lunes 1 de Octubre'  
    WHEN SUBSTR(PATERNO,1,1) BETWEEN 'K' AND 'N' THEN 'Lunes 8 de Octubre'
    WHEN SUBSTR(PATERNO,1,1) BETWEEN 'O' AND 'S' THEN 'Lunes 22 de Octubre'
    ELSE 'Miercoles 24 de Octubre'
END || ' del 2018' AS "FECHA DE EVALUACION" 
FROM EMPLEADO
ORDER BY PATERNO DESC;

